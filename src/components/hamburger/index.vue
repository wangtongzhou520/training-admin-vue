<template>
  <div class="hamburger-container" @click="toggleClick">
    <svg-icon class="hamburger" :icon="icon"></svg-icon>
  </div>
</template>

<script setup>
import { computed } from 'vue'
import { systemUseStore } from '@/stores/system'

const systemStore = systemUseStore()
// 点击事件
const toggleClick = () => {
  systemStore.triggerSidebarOpened(systemStore.sidebarOpened)
}
// 图标绑定
const icon = computed(() => {
  return systemStore.sidebarOpened ? 'hamburger-opened' : 'hamburger-closed'
})
</script>

<style lang="scss" scoped>
.hamburger-container {
  padding: 0 16px;
  .hamburger {
    display: inline-block;
    vertical-align: middle;
    width: 20px;
    height: 20px;
  }
}
</style>
